<script setup lang="ts">
import { ref, onMounted } from 'vue'
import { invoke } from '@tauri-apps/api/core'

const props = defineProps<{
  gamePath: string
  gameId: string
}>()

const emit = defineEmits<{
  close: []
  saved: []
}>()

const config = ref({
  mainIni: `[main]
# 强制局域网模式 - 屏蔽外网校验，实现免Steam联机
force_lan_only = 1

# 启用局域网广播
enable_lan_broadcast = 1

# 匹配服务器列表类型
# 0=始终返回局域网服务器列表
matchmaking_server_list_actual_type = 0

# 禁用Steam网络检查
disable_steam_network_check = 1

# 低延迟联机模式
lan_broadcast_interval = 300

# 允许未知统计
allow_unknown_stats = 1

# 最大联机人数
max_lobby_players = 32
`
})

async function saveConfig() {
  try {
    const result = await invoke<{ success: boolean; message: string }>('save_main_config', {
      gamePath: props.gamePath,
      config: config.value
    })

    if (result.success) {
      emit('saved')
      emit('close')
    } else {
      alert(`保存失败: ${result.message}`)
    }
  } catch (error) {
    alert(`保存失败: ${error}`)
  }
}

async function loadConfig() {
  try {
    const result = await invoke<{ exists: boolean; content?: string }>('load_main_config', {
      gamePath: props.gamePath
    })

    if (result.exists && result.content) {
      config.value.mainIni = result.content
    }
  } catch (error) {
    // 加载配置失败时静默处理
  }
}

onMounted(() => {
  loadConfig()
})
</script>

